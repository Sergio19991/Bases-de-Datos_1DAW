    DETERMINISTIC
BEGIN

	DECLARE claseCoche VARCHAR(25);
	DECLARE clase VARCHAR(25);
	
	SELECT coches.clase INTO claseCoche
	FROM coches
	WHERE coches.codigo = codigoCoche;
	
