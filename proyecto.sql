DELIMITER //
CREATE PROCEDURE `procedimiento1__proyecto`(IN codigoCoche INT)
BEGIN

	SELECT COUNT(*), coches.marca, coches.modelo
	FROM coches
	INNER JOIN resultados ON coches.codigo = resultados.coche
	WHERE resultados.piloto = codigoCoche
	GROUP BY resultados.coche;

END//
DELIMITER ;

-- Volcando estructura para procedimiento proyecto.procedimiento2__proyecto
DELIMITER //
CREATE PROCEDURE `procedimiento2__proyecto`()
BEGIN

	DECLARE longitudCircuito DECIMAL(10.3);
	DECLARE nombreCircuito VARCHAR(50);
	
	SELECT circuitos.longitud INTO longitudCircuito
	FROM circuitos
	WHERE circuitos.longitud >= ALL (
		SELECT circuitos.longitud
		FROM circuitos
	);
	
	SELECT circuitos.nombre INTO nombreCircuito
	FROM circuitos
	WHERE circuitos.longitud >= ALL (
		SELECT circuitos.longitud
		FROM circuitos
	);
		
	SELECT CONCAT('El Circuito ', nombreCircuito, ' es el más largo, con un total de ',  longitudCircuito, ' kilómetros.') AS 'EL CIRCUITO MÁS LARGO:';
	
END//
