CREATE FUNCTION `funcion1__proyecto`(codigoCoche INT) RETURNS varchar(25) CHARSET utf8mb4
    DETERMINISTIC
BEGIN

	DECLARE claseCoche VARCHAR(25);
	DECLARE clase VARCHAR(25);
	
	SELECT coches.clase INTO claseCoche
	FROM coches
	WHERE coches.codigo = codigoCoche;
	
	if (claseCoche = 'P') then
		SET clase = 'PRODUCCIÓN';
	ELSEIF (claseCoche = 'R') then
		SET clase = 'COMEPETICIÓN';
	ELSEIF (claseCoche = 'S') then
		SET clase = 'SUPERCOCHE';
	END if;
	
	RETURN clase;	 
		
END//
DELIMITER ;

