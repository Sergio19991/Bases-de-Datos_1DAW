CREATE FUNCTION `funcion2__proyecto`(codigoPiloto INT) RETURNS varchar(25) CHARSET utf8mb4
    DETERMINISTIC
BEGIN

	DECLARE posicionPiloto VARCHAR(25);
	DECLARE resultado VARCHAR(25);

	SELECT resultados.posicion INTO posicionPiloto
	FROM resultados
	INNER JOIN pilotos ON resultados.piloto = pilotos.codigo
	WHERE resultados.piloto = codigoPiloto;
	
	case posicionPiloto
		when 1 then
			SET resultado = 'GANADOR';
		when 2 then
			SET resultado = 'GANADOR';
		when 3 then
			SET resultado = 'GANADOR';
		ELSE
			SET resultado = 'PERDEDOR';
	END case;
	
	RETURN resultado;

END//
